export function createDashboardTranslator(language: Language) {
  const locale = language === "en" ? "en-US" : language === "id" ? "id-ID" : "zh-CN"
  const base = (translations[language]?.dashboard || {}) as Record<string, string>
  const enBase = (translations.en?.dashboard || {}) as Record<string, string>
  
  const t = (key: DashboardKey | string) => {
    return (base[key] as string) || (DASHBOARD_TEXT as any)[key]?.[language] || enBase[key] || (DASHBOARD_TEXT as any)[key]?.en || key
  }
  
  const dayLabel = (day: string) => DAY_NAMES[day]?.[language] ?? DAY_NAMES[day]?.en ?? day
  const shortDayLabel = (day: string) => SHORT_DAY_NAMES[day]?.[language] ?? SHORT_DAY_NAMES[day]?.en ?? day
  
  // Create a proxy or a merged object to allow both dt('key') and dt.key
  const translator: any = t
  translator.t = t
  translator.locale = locale
  translator.dayLabel = dayLabel
  translator.shortDayLabel = shortDayLabel
  
  // Add all keys from DASHBOARD_TEXT
  Object.keys(DASHBOARD_TEXT).forEach(key => {
    translator[key] = t(key)
  })
  
  // Add all keys from base translations
  Object.keys(enBase).forEach(key => {
    translator[key] = t(key)
  })
  
  return translator as {
    (key: string): string
    t: (key: string) => string
    locale: string
    dayLabel: (day: string) => string
    shortDayLabel: (day: string) => string
    [key: string]: any
  }
}
